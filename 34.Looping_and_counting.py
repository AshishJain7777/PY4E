word = "banana"
count = 0
for letter in word:  # we used iteration for loop. We assigned word in letter or string into another variable.
    if letter == "a": # whenever letter a comes in a string it will count it and add one to it.
        count = count + 1 # a apperared and count's value changes to 1.
print(count) # after end of the program it will show the last value of count. As per example it will be 3. Because "a" comes three times in this string.