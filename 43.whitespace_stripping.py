greet = "  hello bob " # we stored a string in this variable, but we left some whitespace on the both side of the string.
print(greet.lstrip()) # As we can see we put a function that says lstrip, it can remove whitespace from the leftside of the string.
print(greet.rstrip()) # this function can clear the white space from the right side of the string.

print(greet.strip())  # strip can remove white space from both beginning and the ending.
